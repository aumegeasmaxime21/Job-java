public class Job07 {
    public static void main(String[] args) {


        int b = 5;

        for (int j = 0; j <= b; j++) {

            for (int a = 0; a < b - j; a++) {
                System.out.print(" ");
            }
            for (int i = 0; i <= j; i++) {
                System.out.print("* ");
            }
            System.out.println();
        }







    }
}








